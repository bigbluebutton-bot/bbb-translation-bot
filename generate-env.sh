create_file_if_not_exists() {
  local file_name="$1"
  local content="$2"

  if [ -f "$file_name" ]; then
    echo "File '$file_name' already exists. Skipping creation."
  else
    echo "$content" > "$file_name"
    echo "File '$file_name' created."
  fi
}

